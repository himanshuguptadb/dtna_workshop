-- MAGIC     ```
-- MAGIC
-- MAGIC   In the chat window at right, Databricks Assistant proposes a query.
-- MAGIC
-- MAGIC 8. Let's refine this query. In the chat text-entry field below that suggested query, enter this prompt:
-- MAGIC <br/>
